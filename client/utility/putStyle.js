'use strict';

////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
define(['lodash'], function (_) {
////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////


	console.log("Loading 'utility/putStyle'");


	var PREFIXES = {
		'transform'  : ['webkit', 'moz', 'ms', 'o', ''],
		'perspective': ['webkit', 'moz', 'ms', 'o', '']
	};


	function putStyle(obj, key, val) {
		_.each(PREFIXES[key], function (prefix) {
			obj['-' + prefix + '-' + key] = val;
		});
	}


	return putStyle;


////////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
});/////////////////////////////////////////////////////////////////////////////////////////////////////////////////////
